.macro readmemabs
	
	and r1,addy,#0xE000
	adr lr,0f
//	ldr pc,[m6502_rmem,r1,lsr#11]	//in: addy,r1=addy&0xE000 (for rom_R)
	lsr r1,r1,#11				 //改过，加2行	 >>11
	ldr pc,[m6502_rmem,r1]
				
0:				//out: r0=val (bits 8-31=0 (LSR,ROR,INC,DEC,ASL)), addy preserved for RMW instructions
.endm

