	ldr r2,m6502_s
	strb \x,[r2],#-1
	strb r2,m6502_s
.endm		//r2=?

.macro pop16
			//pop m6502_pc
	ldrb r2,m6502_s
	add r2,r2,#2
	strb r2,m6502_s
	ldr r2,m6502_s
	ldrb r0,[r2],#-1
	orr r2,r2,#0x100
	ldrb m6502_pc,[r2]
	orr m6502_pc,m6502_pc,r0,lsl#8
.endm		//r0,r1=?

